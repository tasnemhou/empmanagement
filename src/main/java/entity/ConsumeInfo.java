package entity;

public class ConsumeInfo {

	private String customerId;
	private String cardNO;
	private String phoneNum;
	private String consumDT;
	private String customer;
	private String consumeTimes;
	private String teller;
	private String cardType;
	private String remark;
	private String recordDt;
	
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getCardNO() {
		return cardNO;
	}
	public void setCardNO(String cardNO) {
		this.cardNO = cardNO;
	}
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}
	public String getConsumDT() {
		return consumDT;
	}
	public void setConsumDT(String consumDT) {
		this.consumDT = consumDT;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getConsumeTimes() {
		return consumeTimes;
	}
	public void setConsumeTimes(String consumeTimes) {
		this.consumeTimes = consumeTimes;
	}
	public String getTeller() {
		return teller;
	}
	public void setTeller(String teller) {
		this.teller = teller;
	}
	public String getCardType() {
		return cardType;
	}
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getRecordDt() {
		return recordDt;
	}
	public void setRecordDt(String recordDt) {
		this.recordDt = recordDt;
	}
	
}
